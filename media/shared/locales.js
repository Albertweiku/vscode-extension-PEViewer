// 当前语言
let currentLanguage = "en";

// 获取本地化字符串
function t(key, params) {
  const langData = locales[currentLanguage];
  let text = key;
  if (langData && langData[key]) {
    text = langData[key];
  }

  // 参数替换
  if (params) {
    Object.keys(params).forEach((paramKey) => {
      text = text.replace(
        new RegExp(`\\{${paramKey}\\}`, "g"),
        params[paramKey],
      );
    });
  }

  return text;
}

